-- TABLA FICHA TECNICA
CREATE TABLE FichaTecnica (
    idFicha NUMBER(10) PRIMARY KEY,
    VIN VARCHAR2(50) NOT NULL,
    cilindrada NUMBER(6) NOT NULL,
    potenciaCV NUMBER(4) NOT NULL,
    potenciaKw NUMBER(4,1) NOT NULL,
    numeroPlazas NUMBER(2) NOT NULL,
    mma NUMBER(6) NOT NULL,
    dimensiones VARCHAR2(100) NOT NULL,
    fechaFabricacion DATE NOT NULL,
    idTipoVehiculo NUMBER(3),
    idVehiculo NUMBER(10),
    idCombustible NUMBER(3),
    CONSTRAINT FK_FICHA_TIPOVEHICULO FOREIGN KEY (idTipoVehiculo) REFERENCES TipoVehiculo(idTipoVehiculo),
    CONSTRAINT FK_FICHA_VEHICULO FOREIGN KEY (idVehiculo) REFERENCES Vehiculo(idvehiculo),
    CONSTRAINT FK_FICHA_COMBUSTIBLE FOREIGN KEY (idCombustible) REFERENCES TipoCombustible(idCombustible),
    CONSTRAINT uq_ficha_vehiculo UNIQUE (idVehiculo)
) TABLESPACE dgt_data;


CREATE TABLE FactorEmisiones (
    id        NUMBER
                 GENERATED BY DEFAULT AS IDENTITY
                 CONSTRAINT pk_factor_emisiones PRIMARY KEY,
    co2Min    NUMBER(4)      NOT NULL,
    co2Max    NUMBER(4)      NOT NULL,
    f_emis    NUMBER(3,2)    NOT NULL
) TABLESPACE dgt_data;

CREATE TABLE Emisiones (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT pk_emisiones PRIMARY KEY,
    emisionesCO2 NUMBER(4) NOT NULL,
    idVehiculo NUMBER NOT NULL,
    idFactorEmisiones NUMBER NOT NULL,
    CONSTRAINT fk_emisiones_vehiculo
        FOREIGN KEY (idVehiculo)
        REFERENCES Vehiculo(idVehiculo),
    CONSTRAINT fk_emisiones_factor
        FOREIGN KEY (idFactorEmisiones)
        REFERENCES FactorEmisiones(id),
    CONSTRAINT uq_emisiones_vehiculo
        UNIQUE (idVehiculo)
) TABLESPACE dgt_data;



