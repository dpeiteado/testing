-- HOMOLOGACION
CREATE TABLE homologacion (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT pk_homologacion PRIMARY KEY,
    contrasenahomologacion VARCHAR2(100) NOT NULL,
    autoridadhomologacion VARCHAR2(100) NOT NULL,
    fabricante VARCHAR2(100) NOT NULL,
    fechahomologacion DATE NOT NULL,
    idvehiculo NUMBER NOT NULL,
    CONSTRAINT fk_homologacion_vehiculo FOREIGN KEY (idvehiculo) REFERENCES vehiculo (idvehiculo),
    CONSTRAINT uq_homologacion_vehiculo UNIQUE (idvehiculo)
) TABLESPACE dgt_data;



-- TIPO DE VEHICULO
CREATE TABLE TipoVehiculo (
    idTipoVehiculo      NUMBER(3)       PRIMARY KEY,
    descripcionVehiculo VARCHAR2(50)   NOT NULL,
